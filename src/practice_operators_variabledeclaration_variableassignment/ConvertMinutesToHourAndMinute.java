package practice_operators_variabledeclaration_variableassignment;

public class ConvertMinutesToHourAndMinute {

	public static void main(String[] args) {
		int totalMinutes = 135;
		int hours = totalMinutes/60;
		int RemainingMinutes = totalMinutes%60;
		System.out.println(totalMinutes  + " Minutes = " + hours + " hours and " + RemainingMinutes + " minutes " );
		

	}

}

package practice_operators_variabledeclaration_variableassignment;

public class Arithmetic_calculation {

	public static void main(String[] args) {
		int a=10, b=20;
		System.out.println("Addition of A & B : " + (a+b));
		System.out.println("Subtraction of A & B : " + (a-b));
		System.out.println("Division of A & B " + (a/b)); // to find quotient
		System.out.println("Modulus of A & B : " + (a%b)); // to find remainder
		System.out.println("Multiplication of A & B : " + (a*b));

	}

}

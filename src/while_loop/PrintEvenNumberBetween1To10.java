package while_loop;

public class PrintEvenNumberBetween1To10 {

	public static void main(String[] args) {
		int i=2;
		while(i<=10) {
			if(i%2==0) {
				System.out.print(i);
			}
			i++;
		}
		System.out.println(" ");
		
		int a=2;
		while(a<=10) {
			
			System.out.println(a);
			a+=2;
		}

	}

}

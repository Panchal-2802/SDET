package while_loop;

public class DifferentiateOddOrEven_while_loop {

	public static void main(String[] args) {
		int i=1;
		while(i<=50) {
			if(i%2==0) {
				System.out.println("Even Number : " + i);
			}
			else {
				System.out.println("Odd Number : " + i);
			}
			i++;
		}
		

	}

}

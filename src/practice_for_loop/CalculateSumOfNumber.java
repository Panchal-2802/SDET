package practice_for_loop;

public class CalculateSumOfNumber {
// calculate the sum of number from 1 to n=50
	public static void main(String[] args) {
		int sum = 0;
		for (int i=1; i<=50; i++) {
			sum+=i;//sum = sum+i
			
		}
		System.out.println("The sum of 1 to 50 number is : " + sum);

	}

}

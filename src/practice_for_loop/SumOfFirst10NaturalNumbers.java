package practice_for_loop;

public class SumOfFirst10NaturalNumbers {

	public static void main(String[] args) {
		int sum = 0;
		for(int i=1; i<=10; i++) {
			 sum+= i; // means sum = sum+i
			                      //=  0+1
			                           //1+2
			
		}
		System.out.println("Sum of first 10 natural number : " + sum);

	}

}

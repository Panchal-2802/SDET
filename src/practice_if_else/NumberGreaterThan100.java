package practice_if_else;

public class NumberGreaterThan100 {

	public static void main(String[] args) {
		int a = 200;
		if (a>100) {
			System.out.println("The number " + a + " is greater than " + 100);
		}
		else {
			System.out.println("The number " + a + " is not greater than " + 100);
		}

	}

}

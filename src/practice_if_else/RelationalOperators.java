package practice_if_else;

public class RelationalOperators {

	public static void main(String[] args) {
		int a = 10, b = 20;
		if (a == b ) {
			System.out.println(" Both A and B are equal");
		}
		else {
			System.out.println(" Both digits are not equal ");
		}
		
		int c = 20 , d = 25 ;
		if (c>d) {
			System.out.println("Print C is greater than D");
		}
		else {
			System.out.println("Print D is greater than C");
		}
		
		int e = 20 , f = 25 ;
		if (e<f) {
			System.out.println("Print E is greater than F");
		}
		else {
			System.out.println("Print F is greater than E");
		}

	}

}
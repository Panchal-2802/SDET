package java_operators;

public class Arithmetic_operators {

	public static void main(String[] args) {
		// Arithmetic operators :- +, -, *, /, %
		
		int a=10,  b = 20;
		System.out.println("Sum of a and b is :" + (a+b));
		System.out.println("Sum of a subtract b is :" + (a-b));
		System.out.println("Sum of a multiply b is :" + (a*b));
		System.out.println("Sum of a divide b is :" + (a/b)); // this will return the quotient value
		System.out.println("Modulus division of a and b is :" + (a%b)); //this will return the remainder value
	}

}

package java_operators;

public class Assigment_operators {

	public static void main(String[] args) {
		// operators :- =, +=, -=, *=, /=, %=
		
		int a=20;
		a+=5;
		System.out.println(a);
		
		int b = 36;
		b-=5;
		System.out.println(b);
		
		int c=40;
		c*=5;
		System.out.println(c);
		
		int d =25;
		d/=5;
		System.out.println(d);
		
		int e=25;
		e%=5;
		System.out.println(e);
		
		// unary operators means a single variable is used
		// binary operators means more than one variable is used
		// teranry operators means more than two variable is used

	}

}
